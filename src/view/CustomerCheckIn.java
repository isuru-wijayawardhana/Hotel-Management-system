    private void bookRoomBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookRoomBtnActionPerformed
        // TODO add your handling code here:
        
        String name=nameTF.getText();
        String mobileNumber=mobleNumberTF.getText();
        String nationality=nationalityTF.getText();
        String gender=(String)genderCMB.getSelectedItem();
        String email=emailTF.getText();
        String idProof=idProofTF.getText();
        String address=addressTF.getText();
        String checkIn=checkInDateTF.getText();
        String bed=(String)bedCMB.getSelectedItem();
        String roomType=(String)roomTypeCMB.getSelectedItem();
        String roomNumber=(String)roomNumberCMB.getSelectedItem();
        String price=priceTF.getText();
        
        if(!price.equals("")&&!name.equals("")&&!mobleNumberTF.equals("")&&!nationalityTF.equals("")&&!emailTF.equals("")&&!idProofTF.equals("")&&!addressTF.equals("")){
            CustomerCheckInController.UpdateBooking(roomNumber);            
            CustomerCheckInController.AddCustomer(name, mobileNumber, nationality, gender, email, idProof, address, checkIn, roomNumber, bed, roomType, price);
            clearField();
        }else{
            CustomerCheckInController.AddCustomerNotFill();
        }
        
    }//GEN-LAST:event_bookRoomBtnActionPerformed

