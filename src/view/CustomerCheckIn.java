    public void roomDetails(){
        roomNumberCMB.removeAllItems();
        priceTF.setText("");
        bed=(String)bedCMB.getSelectedItem();
        roomType=(String)roomTypeCMB.getSelectedItem();
        try {
            ResultSet rs=new DBCustomerCheckIn().GetRoomDetails(bed,roomType);
            
            while(rs.next()){
                roomNumberCMB.addItem(rs.getString(1));
            }
            DBConnection.closeCon();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
